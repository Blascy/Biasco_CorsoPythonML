"""
    La coerenza funzionale è la capacità delle funzioni, metodi o le operazioni di essere eseguite in  modo uniforme e senza ambiguità. 
    Offrono:
    Affidabilità: Funzioni coerenti sono prevedibili, il che le rende più facili da testare e depurare.
    Sicurezza: L'assenza di effetti collaterali riduce il rischio di bug difficili da tracciare e risolvere.
    Manutenibilità: Le funzioni coerenti possono essere facilmente comprese, modificate e combinate, migliorando la modularità del codice.
    Parallelismo: Le funzioni coerenti sono più facili da eseguire in parallelo, poiché non interferiscono con lo stato condiviso.
    """